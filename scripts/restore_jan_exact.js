// 스크린샷에 표시된 품목 목록
const screenshotProducts = new Set(Object.keys(originalHP));

async function run() {
  const sdSnap = await getDocs(collection(db, "salesDaily"));
  let updated = 0;

  for (const d of sdSnap.docs) {
    const data = d.data();
    if (!data.date || !data.date.startsWith("2026-01")) continue;

    const product = data.product;
    if (!screenshotProducts.has(product)) continue;

    const productData = originalHP[product];
    const correctHP = productData[data.date] || 0;
    const currentHP = data.housePurchase || 0;

    if (currentHP !== correctHP) {
      await updateDoc(doc(db, "salesDaily", d.id), { housePurchase: correctHP });
      console.log(`  ${data.date} | ${product} | ${currentHP} → ${correctHP}`);
      updated++;
    }
  }

  console.log(`\n총 ${updated}건 수정 완료!`);
  process.exit(0);
}

run().catch(err => { console.error(err); process.exit(1); });
