const fields = ["supplyPrice","totalMargin","quantity","adCost","housePurchase","solution"];

async function run() {
  const sdSnap = await getDocs(collection(db, "salesDaily"));
  let updated = 0;
  const screenshotProducts = new Set(Object.keys(data));

  for (const d of sdSnap.docs) {
    const entry = d.data();
    if (!entry.date || !entry.date.startsWith("2026-01")) continue;
    if (!screenshotProducts.has(entry.product)) continue;

    const productData = data[entry.product];
    const vals = productData[entry.date] || [0,0,0,0,0,0];

    const updates = {};
    let changed = false;
    for (let i = 0; i < fields.length; i++) {
      const current = entry[fields[i]] || 0;
      if (current !== vals[i]) {
        updates[fields[i]] = vals[i];
        changed = true;
      }
    }

    if (changed) {
      await updateDoc(doc(db, "salesDaily", d.id), updates);
      const changedFields = Object.keys(updates).join(",");
      console.log(`  ${entry.date} | ${entry.product} | ${changedFields}`);
      updated++;
    }
  }

  console.log(`\n총 ${updated}건 수정 완료!`);
  process.exit(0);
}

run().catch(err => { console.error(err); process.exit(1); });
