const fieldNames = ["supplyPrice","totalMargin","quantity","adCost","housePurchase","solution"];
const screenshotProducts = new Set(Object.keys(data));

async function run() {
  const sdSnap = await getDocs(collection(db, "salesDaily"));
  let zeroed = 0;
  let updated = 0;

  // Step 1: 2월 전체 항목을 0으로 초기화 (2월만!)
  console.log("=== Step 1: 2월 전체 초기화 ===");
  for (const d of sdSnap.docs) {
    const entry = d.data();
    if (!entry.date || !entry.date.startsWith("2026-02")) continue;

    const updates = {};
    let changed = false;
    for (const f of fieldNames) {
      if ((entry[f] || 0) !== 0) {
        updates[f] = 0;
        changed = true;
      }
    }
    if (changed) {
      await updateDoc(doc(db, "salesDaily", d.id), updates);
      zeroed++;
    }
  }
  console.log(`  ${zeroed}건 초기화\n`);

  // Step 2: 스크린샷 데이터 입력 (2월만!)
  console.log("=== Step 2: 스크린샷 데이터 입력 ===");
  // Re-read after zeroing
  const sdSnap2 = await getDocs(collection(db, "salesDaily"));
  for (const d of sdSnap2.docs) {
    const entry = d.data();
    if (!entry.date || !entry.date.startsWith("2026-02")) continue;
    if (!screenshotProducts.has(entry.product)) continue;

    const productData = data[entry.product];
    const vals = productData[entry.date];
    if (!vals) continue; // 스크린샷에 없는 날짜 = 0 유지

    const updates = {};
    let changed = false;
    for (let i = 0; i < fieldNames.length; i++) {
      if (vals[i] !== 0) {
        updates[fieldNames[i]] = vals[i];
        changed = true;
      }
    }
    if (changed) {
      await updateDoc(doc(db, "salesDaily", d.id), updates);
      console.log(`  ${entry.date} | ${entry.product}`);
      updated++;
    }
  }

  console.log(`\n총 ${updated}건 입력 완료!`);
  process.exit(0);
}

run().catch(err => { console.error(err); process.exit(1); });
