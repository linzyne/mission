async function restore() {
  console.log('=== 1월 salesDaily 복원 시작 ===');

  // 1단계: 기존 1월 데이터 전부 삭제
  console.log('1단계: 기존 1월 데이터 삭제...');
  const snapshot = await getDocs(query(collection(db, 'salesDaily')));
  const janDocs = snapshot.docs.filter(d => {
    const data = d.data();
    return data.date && data.date.startsWith('2026-01');
  });
  console.log(`  삭제 대상: ${janDocs.length}개`);

  // Firestore batch는 500개 제한
  for (let i = 0; i < janDocs.length; i += 400) {
    const batch = writeBatch(db);
    const chunk = janDocs.slice(i, i + 400);
    chunk.forEach(d => batch.delete(doc(db, 'salesDaily', d.id)));
    await batch.commit();
    console.log(`  삭제 완료: ${Math.min(i + 400, janDocs.length)}/${janDocs.length}`);
  }

  // 2단계: 새 데이터 입력
  console.log(`2단계: 새 데이터 입력 (${janData.length}개)...`);
  for (let i = 0; i < janData.length; i += 400) {
    const batch = writeBatch(db);
    const chunk = janData.slice(i, i + 400);
    chunk.forEach(entry => {
      const docId = `${entry.date}_${entry.product}`;
      batch.set(doc(db, 'salesDaily', docId), entry);
    });
    await batch.commit();
    console.log(`  입력 완료: ${Math.min(i + 400, janData.length)}/${janData.length}`);
  }

  // 검증
  console.log('\n=== 검증 ===');
  const products = [...new Set(janData.map(d => d.product))];
  for (const p of products) {
    const entries = janData.filter(d => d.product === p);
    const total = {
      supply: entries.reduce((s, e) => s + e.supplyPrice, 0),
      margin: entries.reduce((s, e) => s + e.totalMargin, 0),
      qty: entries.reduce((s, e) => s + e.quantity, 0),
      ad: entries.reduce((s, e) => s + e.adCost, 0),
      hp: entries.reduce((s, e) => s + e.housePurchase, 0),
      sol: entries.reduce((s, e) => s + e.solution, 0),
    };
    console.log(`${p}: 공급가=${total.supply.toLocaleString()} 마진=${total.margin.toLocaleString()} 수량=${total.qty} 광고비=${total.ad.toLocaleString()} 가구매=${total.hp.toLocaleString()} 슬롯=${total.sol.toLocaleString()}`);
  }

  console.log('\n=== 복원 완료! ===');
  process.exit(0);
}

restore().catch(err => { console.error('에러:', err); process.exit(1); });
