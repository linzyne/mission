async function restore() {
  console.log('=== 2월 salesDaily 복원 시작 ===');

  // 1단계: 기존 2월 데이터 삭제
  console.log('1단계: 기존 2월 데이터 삭제...');
  const snapshot = await getDocs(query(collection(db, 'salesDaily')));
  const febDocs = snapshot.docs.filter(d => {
    const data = d.data();
    return data.date && data.date.startsWith('2026-02');
  });
  console.log(`  삭제 대상: ${febDocs.length}개`);

  for (let i = 0; i < febDocs.length; i += 400) {
    const batch = writeBatch(db);
    febDocs.slice(i, i + 400).forEach(d => batch.delete(doc(db, 'salesDaily', d.id)));
    await batch.commit();
  }

  // 2단계: 새 데이터 입력
  console.log(`2단계: 새 데이터 입력 (${febData.length}개)...`);
  for (let i = 0; i < febData.length; i += 400) {
    const batch = writeBatch(db);
    febData.slice(i, i + 400).forEach(entry => {
      const docId = `${entry.date}_${entry.product}`;
      batch.set(doc(db, 'salesDaily', docId), entry);
    });
    await batch.commit();
  }

  // 검증
  console.log('\n=== 검증 ===');
  const products = [...new Set(febData.map(d => d.product))];
  for (const p of products) {
    const entries = febData.filter(d => d.product === p);
    const t = {
      sp: entries.reduce((s, e) => s + e.supplyPrice, 0),
      tm: entries.reduce((s, e) => s + e.totalMargin, 0),
      qty: entries.reduce((s, e) => s + e.quantity, 0),
      ad: entries.reduce((s, e) => s + e.adCost, 0),
      hp: entries.reduce((s, e) => s + e.housePurchase, 0),
      sol: entries.reduce((s, e) => s + e.solution, 0),
    };
    console.log(`${p}: 공급가=${t.sp.toLocaleString()} 마진=${t.tm.toLocaleString()} 수량=${t.qty} 광고비=${t.ad.toLocaleString()} 가구매=${t.hp.toLocaleString()} 슬롯=${t.sol.toLocaleString()}`);
  }

  console.log('\n=== 완료! ===');
  process.exit(0);
}

